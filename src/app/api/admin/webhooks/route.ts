// src/app/api/admin/webhooks/route.ts
import { NextRequest, NextResponse } from "next/server";
import { getApps, initializeApp, cert } from "firebase-admin/app";
import { getFirestore } from "firebase-admin/firestore";

// Firebase Admin init
function getFirebaseApp() {
  if (getApps().length > 0) {
    return getApps()[0]!;
  }

  const projectId = process.env.FIREBASE_PROJECT_ID;
  const clientEmail = process.env.FIREBASE_CLIENT_EMAIL;
  const privateKey = process.env.FIREBASE_PRIVATE_KEY?.replace(/\\n/g, "\n");

  if (!projectId || !clientEmail || !privateKey) {
    throw new Error("Firebase credentials missing");
  }

  return initializeApp({
    credential: cert({
      projectId,
      clientEmail,
      privateKey,
    }),
  });
}

export async function GET(req: NextRequest) {
  try {
    // Admin authentication
    const adminKey = req.headers.get('admin-key');
    if (adminKey !== process.env.ADMIN_API_KEY) {
      return NextResponse.json(
        { error: 'Unauthorized - invalid admin key' },
        { status: 401 }
      );
    }

    // Get query parameters
    const { searchParams } = new URL(req.url);
    const limit = parseInt(searchParams.get('limit') || '50');
    const offset = parseInt(searchParams.get('offset') || '0');

    // Initialize Firebase
    const app = getFirebaseApp();
    const db = getFirestore(app);

    // Query subscription_events collection
    let query = db.collection('subscription_events')
      .orderBy('timestamp', 'desc')
      .limit(limit);

    if (offset > 0) {
      // For pagination, we'd need to implement cursor-based pagination
      // For now, we'll use limit/offset approximation
      query = query.offset(offset);
    }

    const snapshot = await query.get();
    
    const events = snapshot.docs.map(doc => ({
      id: doc.id,
      ...doc.data(),
      // Add replay status indicators
      has_been_replayed: !!doc.data().last_replay_at,
      replay_count: doc.data().replay_count || 0
    }));

    // Get total count for pagination
    const totalSnapshot = await db.collection('subscription_events').count().get();
    const total = totalSnapshot.data().count;

    return NextResponse.json({
      events,
      pagination: {
        total,
        limit,
        offset,
        has_more: (offset + limit) < total
      }
    });

  } catch (error) {
    console.error('[webhooks API] Error:', error);
    
    return NextResponse.json(
      { 
        error: 'Internal server error',
        details: error instanceof Error ? error.message : 'Unknown error'
      },
      { status: 500 }
    );
  }
}